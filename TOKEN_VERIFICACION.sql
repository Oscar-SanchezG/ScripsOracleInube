CREATE OR REPLACE TRIGGER TRG_TOKEN_VERIFICACION_AUD AFTER INSERT OR UPDATE ON TOKEN_VERIFICACION FOR EACH ROW
DECLARE
    V_ACCION VARCHAR2(20);
    V_FECHA_CREACION DATE := NULL;
    V_FECHA_MODIFICACION DATE := NULL;
BEGIN

    -- INSERT
    IF INSERTING THEN
        V_ACCION := 'INSERT';
        V_FECHA_CREACION := SYSDATE;

    -- UPDATE
    ELSIF UPDATING THEN
        V_FECHA_MODIFICACION := SYSDATE;
        V_FECHA_CREACION := :OLD.FECHA_ALTA;
        -- Detectar borrado lógico
        IF :OLD.ESTADO = 0 AND :NEW.ESTADO = 99 THEN
            V_ACCION := 'DEACTIVATE';
        ELSIF :OLD.ESTADO = 99 AND :NEW.ESTADO =0 THEN
            V_ACCION := 'ACTIVATE';

        ELSE
            V_ACCION := 'UPDATE';
        END IF;

    END IF;

    INSERT INTO TOKEN_VERIFICACION_BIT (ID_TOKEN_VERIFICACION_BIT, ID_TOKEN_VERIFICACION, ID_PERSONA, TOKEN, ID_TIPO_TOKEN,
                                        USADO, CADUCADO, FECHA_EXPIRACION,FECHA_USO, ESTADO, NOTAS,
                                        FECHA_CREACION , FECHA_MODIFICACION, ACCION, IP )
    VALUES (
        SYS_GUID(),
        :NEW.ID_TOKEN_VERIFICACION,
        :NEW.ID_PERSONA,
        :NEW.TOKEN,
        :NEW.ID_TIPO_TOKEN,
        :NEW.USADO,
        :NEW.CADUCADO,
        :NEW.FECHA_EXPIRACION,
        :NEW.FECHA_USO,
        :NEW.ESTADO,
        :NEW.NOTAS,
        V_FECHA_CREACION,
        V_FECHA_MODIFICACION,
        V_ACCION,
        FN_GET_IP
    );

END;

