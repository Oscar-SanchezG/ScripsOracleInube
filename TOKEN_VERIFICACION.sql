CREATE TABLE TOKEN_VERIFICACION (
    ID_TOKEN_VERIFICACION VARCHAR2(20) NOT NULL,
    ID_PERSONA VARCHAR2(36) NOT NULL,
    TOKEN VARCHAR2(255) NOT NULL,
    ID_TIPO_TOKEN VARCHAR2(20) NOT NULL,
    USADO NUMBER(1) DEFAULT 0 NOT NULL,
    CADUCADO NUMBER(1) DEFAULT 0 NOT NULL,
    FECHA_ALTA TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    FECHA_EXPIRACION TIMESTAMP NOT NULL,
    FECHA_USO TIMESTAMP,
    ESTADO NUMBER(2) DEFAULT 0 NOT NULL,
    NOTAS VARCHAR2(100),

    CONSTRAINT PK_TOKEN_VERIFICACION PRIMARY KEY (ID_TOKEN_VERIFICACION),

    CONSTRAINT FK_TOKENV_USUARIO
        FOREIGN KEY (ID_PERSONA)
        REFERENCES PERSONA(ID_PERSONA),

    CONSTRAINT FK_TIPO_TOKENV
        FOREIGN KEY (ID_TIPO_TOKEN)
        REFERENCES CAT_TIPO_TOKEN(ID_TIPO_TOKEN),

    CONSTRAINT UK_TOKEN UNIQUE (TOKEN),

    CONSTRAINT CK_TOKEN_USADO CHECK (USADO IN (0,1)),
    CONSTRAINT CK_TOKEN_CADUCADO CHECK (CADUCADO IN (0,1))
);

COMMENT ON TABLE TOKEN_VERIFICACION IS 'Tabla catálogo de tipo de token';
COMMENT ON COLUMN TOKEN_VERIFICACION.ID_TOKEN_VERIFICACION IS 'Clave del token verificacion';
COMMENT ON COLUMN TOKEN_VERIFICACION.ID_PERSONA IS 'Clave de la persona';
COMMENT ON COLUMN TOKEN_VERIFICACION.TOKEN IS 'Token de verificacion';
COMMENT ON COLUMN TOKEN_VERIFICACION.ID_TIPO_TOKEN IS 'Id Tipo de token';
COMMENT ON COLUMN TOKEN_VERIFICACION.USADO IS 'Indica si el token ya fue usado';
COMMENT ON COLUMN TOKEN_VERIFICACION.CADUCADO IS 'Indica si el token ya fue caducado';
COMMENT ON COLUMN TOKEN_VERIFICACION.FECHA_ALTA IS 'Fecha de creacion del token';
COMMENT ON COLUMN TOKEN_VERIFICACION.FECHA_EXPIRACION IS 'Fecha de expiracion del token';
COMMENT ON COLUMN TOKEN_VERIFICACION.FECHA_USO IS 'Fecha de uso del token';
COMMENT ON COLUMN TOKEN_VERIFICACION.NOTAS IS 'Notas del token';

CREATE SEQUENCE SEQ_TOKEN_VERIFICACION START WITH 0 INCREMENT BY 1 MINVALUE 0 NOCACHE NOCYCLE;

CREATE OR REPLACE TRIGGER TRG_TOKEN_VERIFICACION_ID
BEFORE INSERT ON TOKEN_VERIFICACION
FOR EACH ROW
BEGIN
    IF :NEW.ID_TOKEN_VERIFICACION IS NULL THEN
        :NEW.ID_TOKEN_VERIFICACION := 'TKV-' || LPAD(SEQ_TOKEN_VERIFICACION.NEXTVAL, 9, '0');
    END IF;
END;
/


