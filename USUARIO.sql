CREATE OR REPLACE VIEW VW_USUARIOS_ACTIVOS AS
SELECT
    U.USER_ID,
    P.ID_PERSONA,
    P.NOMBRE,
    P.CORREO,
    U.PASSWORD,
    P.TELEFONO,
    R.ID_ROL,
    R.NOMBRE_ROL
FROM USUARIO U
INNER JOIN PERSONA P
    ON U.ID_PERSONA = P.ID_PERSONA
INNER JOIN CAT_ROL R
    ON U.ID_ROL = R.ID_ROL
WHERE U.ESTADO = 0
  AND P.ESTADO = 0
  AND R.ESTADO = 0
  AND P.VERIFICADO = 0;


