CREATE TABLE PERSONA (
    ID_PERSONA VARCHAR2(20) NOT NULL,
    NOMBRE VARCHAR2(100) NOT NULL,
    APATERNO VARCHAR2(100) NOT NULL,
    AMATERNO VARCHAR2(100) NOT NULL,
    TELEFONO VARCHAR2(20),
    CORREO VARCHAR2(100) NOT NULL,
    UUID_CIUDAD VARCHAR2(36)  NOT NULL,
    UUID_ESTADO VARCHAR2(36) NOT NULL,
    ESTADO NUMBER(2) DEFAULT 0 NOT NULL,
    FECHA_ALTA DATE DEFAULT SYSDATE,
    VERIFICADO NUMBER(2) DEFAULT 99 NOT NULL,
    CONSTRAINT PK_PERSONA PRIMARY KEY (ID_PERSONA),
    CONSTRAINT UK_PERSONA_CORREO UNIQUE (CORREO),
    CONSTRAINT FK_CAT_PERSONA_ESTADO FOREIGN KEY (UUID_ESTADO)
    REFERENCES CAT_ESTADO(UUID_ESTADO),
    CONSTRAINT FK_CAT_PERSONA_CIUDAD FOREIGN KEY (UUID_CIUDAD)
    REFERENCES CAT_CIUDAD(UUID_CIUDAD),
    CONSTRAINT CK_PERSONA_ESTADO CHECK (ESTADO IN (0, 99)),
    CONSTRAINT CK_PERSONA_VERIFICADO CHECK (VERIFICADO IN (0, 99))
);

COMMENT ON TABLE PERSONA IS 'Tabla que almacena información personal';
COMMENT ON COLUMN PERSONA.ID_PERSONA IS 'Id único de persona';
COMMENT ON COLUMN PERSONA.NOMBRE IS 'Nombre completo';
COMMENT ON COLUMN PERSONA.APATERNO IS 'Apellido paterno';
COMMENT ON COLUMN PERSONA.AMATERNO IS 'Apellido materno';
COMMENT ON COLUMN PERSONA.TELEFONO IS 'Número telefónico';
COMMENT ON COLUMN PERSONA.CORREO IS 'Correo electrónico para login';
COMMENT ON COLUMN PERSONA.UUID_CIUDAD IS 'Id de la ciudad';
COMMENT ON COLUMN PERSONA.UUID_ESTADO IS 'Id del estado';
COMMENT ON COLUMN PERSONA.ESTADO IS '0=Activo, 99=Inactivo';
COMMENT ON COLUMN PERSONA.FECHA_ALTA IS 'Fecha de alta';
COMMENT ON COLUMN PERSONA.VERIFICADO IS '0=No verificado, 99=Verificado';

CREATE SEQUENCE SEQ_PERSONA START WITH 0 INCREMENT BY 1 MINVALUE 0 NOCACHE NOCYCLE;

CREATE OR REPLACE TRIGGER TRG_PERSONA_ID
BEFORE INSERT ON PERSONA
FOR EACH ROW
BEGIN
    IF :NEW.ID_PERSONA IS NULL THEN
        :NEW.ID_PERSONA := 'PER-' || LPAD(SEQ_PERSONA.NEXTVAL, 6, '0');
    END IF;
END;

