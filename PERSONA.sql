CREATE OR REPLACE TRIGGER TRG_PERSONA_AUD
AFTER INSERT OR UPDATE ON PERSONA
FOR EACH ROW
DECLARE
    V_ACCION VARCHAR2(20);
    V_FECHA_CREACION DATE := NULL;
    V_FECHA_MODIFICACION DATE := NULL;
BEGIN

    -- INSERT
    IF INSERTING THEN
        V_ACCION := 'INSERT';
        V_FECHA_CREACION := SYSDATE;

    -- UPDATE
    ELSIF UPDATING THEN
        V_FECHA_MODIFICACION := SYSDATE;
        V_FECHA_CREACION := :OLD.FECHA_ALTA;
        -- Detectar borrado lógico
        IF :OLD.ESTADO = 0 AND :NEW.ESTADO = 99 THEN
            V_ACCION := 'DEACTIVATE';
        ELSIF :OLD.ESTADO = 99 AND :NEW.ESTADO =0 THEN
            V_ACCION := 'ACTIVATE';
        ELSIF :OLD.VERIFICADO = 99 AND :NEW.VERIFICADO =0 THEN
            V_ACCION := 'VERIFIED';
        ELSIF :OLD.VERIFICADO = 0 AND :NEW.VERIFICADO =99 THEN
            V_ACCION := 'UNVERIFIED';
        ELSE
            V_ACCION := 'UPDATE';
        END IF;

    END IF;

    INSERT INTO PERSONA_BIT ( ID_PERSONA_BIT, ID_PERSONA, NOMBRE, APATERNO, AMATERNO, TELEFONO, CORREO, UUID_CIUDAD, UUID_ESTADO,
                              ESTADO, FECHA_ALTA , VERIFICADO , FECHA_CREACION , FECHA_MODIFICACION, ACCION, IP )
    VALUES (
        SYS_GUID(),
        :NEW.ID_PERSONA,
        :NEW.NOMBRE,
        :NEW.APATERNO,
        :NEW.AMATERNO,
        :NEW.TELEFONO,
        :NEW.CORREO,
        :NEW.UUID_CIUDAD,
        :NEW.UUID_ESTADO,
        :NEW.ESTADO,
        :NEW.FECHA_ALTA,
        :NEW.VERIFICADO,
        V_FECHA_CREACION,
        V_FECHA_MODIFICACION,
        V_ACCION,
        FN_GET_IP
    );

END;


